package bd.nmam.collection.business.pojo.beidian;

public class UpgradePojo {
	private String deviceId;
	private String binKey;
	private int upgradeIndex;
	private int upgradePackageSum;
	public String getDeviceId() {
		return deviceId;
	}
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	public String getBinKey() {
		return binKey;
	}
	public void setBinKey(String binKey) {
		this.binKey = binKey;
	}
	public int getUpgradeIndex() {
		return upgradeIndex;
	}
	public void setUpgradeIndex(int upgradeIndex) {
		this.upgradeIndex = upgradeIndex;
	}
	public int getUpgradePackageSum() {
		return upgradePackageSum;
	}
	public void setUpgradePackageSum(int upgradePackageSum) {
		this.upgradePackageSum = upgradePackageSum;
	}
}
