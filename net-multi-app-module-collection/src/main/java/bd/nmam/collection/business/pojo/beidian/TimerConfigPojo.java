package bd.nmam.collection.business.pojo.beidian;

public class TimerConfigPojo {
	private String chNum;
	private String chSta;
	private String chAttr;
	private String ctrlID;
	private String time;
	private String function;
	public String getChNum() {
		return chNum;
	}
	public void setChNum(String chNum) {
		this.chNum = chNum;
	}
	public String getChSta() {
		return chSta;
	}
	public void setChSta(String chSta) {
		this.chSta = chSta;
	}
	public String getChAttr() {
		return chAttr;
	}
	public void setChAttr(String chAttr) {
		this.chAttr = chAttr;
	}
	public String getCtrlID() {
		return ctrlID;
	}
	public void setCtrlID(String ctrlID) {
		this.ctrlID = ctrlID;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getFunction() {
		return function;
	}
	public void setFunction(String function) {
		this.function = function;
	}
}
