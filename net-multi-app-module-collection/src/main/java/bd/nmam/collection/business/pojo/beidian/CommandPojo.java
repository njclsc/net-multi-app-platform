package bd.nmam.collection.business.pojo.beidian;

public class CommandPojo {
	private String platformID;
	private String frameType;
	private String deviceID;
	private String frameTime;
	private String ctrlCount;
	private DOPojo DO;
	private DIPojo DI;
	private AIPojo AI;
	private DOConfigPojo DOConfig;
	private CtrlDataPojo CtrlData;
	private TimerConfigPojo TimerConfig;
	
	public TimerConfigPojo getTimerConfig() {
		return TimerConfig;
	}
	public void setTimerConfig(TimerConfigPojo timerConfig) {
		TimerConfig = timerConfig;
	}
	public DOConfigPojo getDOConfig() {
		return DOConfig;
	}
	public void setDOConfig(DOConfigPojo dOConfig) {
		DOConfig = dOConfig;
	}
	public CtrlDataPojo getCtrlData() {
		return CtrlData;
	}
	public void setCtrlData(CtrlDataPojo ctrlData) {
		CtrlData = ctrlData;
	}
	public AIPojo getAI() {
		return AI;
	}
	public void setAI(AIPojo aI) {
		AI = aI;
	}
	public DIPojo getDI() {
		return DI;
	}
	public void setDI(DIPojo dI) {
		DI = dI;
	}
	public String getPlatformID() {
		return platformID;
	}
	public void setPlatformID(String platformID) {
		this.platformID = platformID;
	}
	public String getFrameType() {
		return frameType;
	}
	public void setFrameType(String frameType) {
		this.frameType = frameType;
	}
	public String getDeviceID() {
		return deviceID;
	}
	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}
	public String getFrameTime() {
		return frameTime;
	}
	public void setFrameTime(String frameTime) {
		this.frameTime = frameTime;
	}
	public String getCtrlCount() {
		return ctrlCount;
	}
	public void setCtrlCount(String ctrlCount) {
		this.ctrlCount = ctrlCount;
	}
	public DOPojo getDO() {
		return DO;
	}
	public void setDO(DOPojo dO) {
		DO = dO;
	}
}
