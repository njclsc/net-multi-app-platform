package bd.nmam.collection.business.pojo.aed;

import java.util.List;

public class FirmwarePojo {

	private String url;
	private String filewareName;
	private String uploadUser;
	private String uploadTime;
	private List<String> devices;
	public String getUrl() {
		return url;
	}

	public String getUploadUser() {
		return uploadUser;
	}

	public void setUploadUser(String uploadUser) {
		this.uploadUser = uploadUser;
	}

	public String getUploadTime() {
		return uploadTime;
	}

	public List<String> getDevices() {
		return devices;
	}

	public void setDevices(List<String> devices) {
		this.devices = devices;
	}

	public void setUploadTime(String uploadTime) {
		this.uploadTime = uploadTime;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getFilewareName() {
		return filewareName;
	}

	public void setFilewareName(String filewareName) {
		this.filewareName = filewareName;
	}
	
}
